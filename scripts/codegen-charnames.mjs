#!/usr/bin/env node
/**
 * Derives a slim codepoint -> Unicode NAME lookup table from the pinned,
 * checked-in `lib/ucd/UnicodeData.txt` (semicolon-delimited UCD data file;
 * field 0 = hex codepoint, field 1 = character name), for the character-map
 * search-by-name feature (engine `character-discovery/characterMap.ts`).
 *
 * Scope: only codepoints 0x0020..0x2FFFF (matches characterMap.ts's
 * SCRIPT_ENUMERATION_END) are considered, and any row whose name begins with
 * "<" is skipped — those are algorithmic/range markers (e.g. "<control>",
 * "<CJK Ideograph, First>", "<Hangul Syllable, First>"), not real per-codepoint
 * names. This naturally excludes CJK ideographs and Hangul syllables (both
 * "not yet supported" per spec Sec 9's Three-group routing stub) along with
 * control characters.
 *
 * Like the sibling lib/ucd/*.txt files consumed by utilities/facet-index/ucd's
 * codegen-ucd.mjs, `lib/ucd/UnicodeData.txt` is SHA-256-pinned in
 * scripts/ucd-version.json and verified before parsing — a mismatch fails
 * loud rather than silently deriving names from a corrupt/tampered file.
 * Ports the verify/--compute-sha idiom of codegen-ucd.mjs / fetch-langtags.mjs.
 *
 * Usage:
 *   node scripts/codegen-charnames.mjs                 verify + generate
 *   node scripts/codegen-charnames.mjs --compute-sha    fill the pin's hash
 *
 * Output:
 *   packages/engine/src/character-discovery/generated/charnames.generated.json
 *   { "<decimal codepoint>": "<NAME>", ... }
 *
 * This file is large (see the console-reported byte size) and is GITIGNORED —
 * regenerated by `pnpm prebuild` (see CLAUDE.md's prebuild bullet list), the
 * same treatment as the langtags raw download.
 */

import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, "..");

const SOURCE_FILE = join(ROOT, "lib", "ucd", "UnicodeData.txt");
const PIN_FILE = join(ROOT, "scripts", "ucd-version.json");
const OUT_DIR = join(ROOT, "packages", "engine", "src", "character-discovery", "generated");
const OUT_FILE = join(OUT_DIR, "charnames.generated.json");

const SCOPE_START = 0x0020;
const SCOPE_END = 0x2ffff;

const computeSha = process.argv.slice(2).includes("--compute-sha");

if (!existsSync(SOURCE_FILE)) {
  fail(`source file not found: ${rel(SOURCE_FILE)}`);
}
if (!existsSync(PIN_FILE)) {
  fail(`pin file not found: ${rel(PIN_FILE)}`);
}

const sourceBytes = readFileSync(SOURCE_FILE);
const actualSha256 = createHash("sha256").update(sourceBytes).digest("hex");

const pin = JSON.parse(readFileSync(PIN_FILE, "utf8"));
const pinEntry = pin.files.find((f) => f.path === "lib/ucd/UnicodeData.txt");
if (!pinEntry) {
  fail(`${rel(PIN_FILE)} does not list lib/ucd/UnicodeData.txt`);
}

if (computeSha) {
  pinEntry.sha256 = actualSha256;
  writeFileSync(PIN_FILE, JSON.stringify(pin, null, 2) + "\n", "utf8");
  console.log(`[OK] wrote lib/ucd/UnicodeData.txt SHA-256 into ${rel(PIN_FILE)}`);
  console.log(`     ${actualSha256}`);
  console.log("[OK] re-run without --compute-sha to generate the lookup.");
  process.exit(0);
}

const expectedSha256 = String(pinEntry.sha256 ?? "").toLowerCase();
if (!expectedSha256 || expectedSha256.startsWith("placeholder")) {
  fail(
    `${rel(PIN_FILE)} has a placeholder SHA-256 for lib/ucd/UnicodeData.txt.\n` +
      "        Run: node scripts/codegen-charnames.mjs --compute-sha",
  );
}
if (actualSha256 !== expectedSha256) {
  fail(
    `SHA-256 mismatch for lib/ucd/UnicodeData.txt — file corrupt or tampered.\n` +
      `        Expected: ${expectedSha256}\n        Got:      ${actualSha256}`,
  );
}
console.log(`[OK] verified lib/ucd/UnicodeData.txt @ Unicode ${pin.unicodeVersion}`);

const text = sourceBytes.toString("utf8");
const names = {};
let kept = 0;
let skipped = 0;

for (const line of text.split(/\r?\n/)) {
  if (!line) continue;
  const fields = line.split(";");
  const hex = fields[0];
  const name = fields[1];
  if (hex === undefined || name === undefined) continue;
  if (name.startsWith("<")) {
    skipped++;
    continue;
  }
  const cp = parseInt(hex, 16);
  if (Number.isNaN(cp) || cp < SCOPE_START || cp > SCOPE_END) {
    skipped++;
    continue;
  }
  names[String(cp)] = name;
  kept++;
}

if (kept === 0) {
  fail("parsed zero character names — wrong source file or scope?");
}

const content = JSON.stringify(names) + "\n";
mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_FILE, content, "utf8");

console.log(
  `[OK] wrote ${rel(OUT_FILE)} — ${kept} names (${skipped} rows skipped), ` +
    `${Buffer.byteLength(content, "utf8")} bytes`,
);

function rel(p) {
  return p.slice(ROOT.length + 1).replace(/\\/g, "/");
}

function fail(msg) {
  console.error(`[ERROR] ${msg}`);
  process.exit(1);
}
